package kr.or.ddit.bnb.prod.vo;

public class ConvenVO {
	private String conven_id   ;
	private String conven_name ;
	private String conven_link ;
	
	public String getConven_id() {
		return conven_id;
	}
	public void setConven_id(String conven_id) {
		this.conven_id = conven_id;
	}
	public String getConven_name() {
		return conven_name;
	}
	public void setConven_name(String conven_name) {
		this.conven_name = conven_name;
	}
	public String getConven_link() {
		return conven_link;
	}
	public void setConven_link(String conven_link) {
		this.conven_link = conven_link;
	}
	
	@Override
	public String toString() {
		return "ConvenVO [conven_id=" + conven_id + ", conven_name=" + conven_name + ", conven_link=" + conven_link
				+ "]";
	}
	
	
	
}
